					<h1 style="text-align: center">Charte du journal lycéen Le Stendhal déchaîné</h1>
					<p>La présente Charte a pour objet de rappeler les principes essentiels de liberté, de fiabilité et de transparence de l’information, ainsi que de préciser les droits et devoirs de chaque journaliste du groupe. Elle s’ajoute au Règlement Intérieur du lycée Stendhal, mais également à tous les accords et chartes qui lui sont d’autorité supérieure et auxquels chaque membre du journal est soumis et s’engage à respecter.
					Les publications du Stendhal Déchaîné ont pour vocation de fournir une information de qualité, précise et vérifiée. Les journalistes se devront de porter un regard critique sur l’information et faire écho au pluralisme des opinions. Par ailleurs, le sensationnalisme, les approximations et partis-pris devront être évités au possible.</p>
					<h2 style="text-align: center">Devoirs :</h2>
					<ol>
						<li>Respecter la vérité, quelles qu’en soient les conséquences</li>
						<li>Défendre la liberté de l’information, du commentaire et de la critique</li>
						<li>Publier seulement des informations d’origine connue, et accompagner si nécessaire des réserves qui s’imposent ; ne pas altérer le sens de ces supports d’informations</li>
						<li>Ne pas user de méthodes déloyales et de manipulation pour obtenir des informations, photographies ou autres documents</li>
						<li>Respecter la vie privée des personnes</li>
						<li>Rectifier toutes informations publiées qui se révèlent inexactes</li>
						<li>Ne pas plagier, calomnier, relayer des rumeurs, accuser sans fondement, discriminer</li>
						<li>Ne pas agir comme publicitaire ou propagandistes</li>
						<li>Refuser toute pression autre que les directives des responsables de la rédaction, n’accepter aucun avantage en raison de la suppression ou de l’ajout d’une information</li>
						<li>Respecter l’image de journal, celle-ci ne pourra être utilisée dans un cadre incompatible aux valeurs du club</li>
					</ol>
					<h2 style="text-align: center">Droits :</h2>
					<ol>
					</li>Au libre accès à toutes sources d’information et droit d’enquêter sur tous les faits qui conditionnent la vie publique ; les affaires publiques ou privées ne pourront y être opposées qu’en raison de motifs clairement exprimés</li>
						<li>Au refus de subordination en cas de directives contraires à la ligne générale de son entreprise journalistique</li>
						<li>Au libre-arbitre. Vous avez le droit d’agir et de vous exprimer d’une manière conforme à vos convictions et à votre conscience</li>
						<li>A être informé(e) de toute décision importante touchant le renvoi ou la promotion d’un journaliste, et cela, avant la prise d’une décision définitive</li>
					</ol>
					<p>Le droit à l’information, la libre expression et la critique, libertés fondamentales de tout être humain, font que le public aura accès à cette Charte afin de pouvoir connaître les faits et opinions concernant l’ensemble des journalistes du Stendhal déchaîné.</p>
